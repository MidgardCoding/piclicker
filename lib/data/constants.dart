  /// Parses duration string (e.g., "10h") to hours as integer
  static int parseDurationToHours(String duration) {
    final regex = RegExp(r'(\d+)h?');
    final match = regex.firstMatch(duration);
    if (match != null) {
      return int.tryParse(match.group(1) ?? '0') ?? 0;
    }
    return 0;
  }
